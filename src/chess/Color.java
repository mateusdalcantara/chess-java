package chess;

/**
 * Enum representing the two colors in a chess game: {@code BLACK} and {@code WHITE}.
 * <p>
 * The {@code Color} enum is used to differentiate the two opposing sides in a chess game.
 * It is commonly associated with chess pieces, where one side will have pieces of one color
 * (either {@code BLACK} or {@code WHITE}) to distinguish the teams or players.
 * </p>
 *
 * @see ChessPiece
 */
public enum Color {
    BLACK,
    WHITE;
}
